import torch
import torch.nn as nn

class LangDyReLU(nn.Module):
    def __init__(self, q_dim, channels, reduction=4, k=2, conv_type='2d'):
        super(LangDyReLU, self).__init__()
        self.channels = channels
        self.k = k
        self.conv_type = conv_type
        assert self.conv_type in ['1d', '2d']

        self.fc1 = nn.Linear(q_dim, channels // reduction)
        self.relu = nn.ReLU(inplace=True)
        self.fc2 = nn.Linear(channels // reduction, 2*k)
        self.sigmoid = nn.Sigmoid()

        self.register_buffer('lambdas', torch.Tensor([1.]*k + [0.5]*k).float())
        self.register_buffer('init_v', torch.Tensor([1.] + [0.]*(2*k - 1)).float())

    def get_relu_coefs(self, q):
        theta = self.fc1(q)
        theta = self.relu(theta)
        theta = self.fc2(theta)
        theta = 2 * self.sigmoid(theta) - 1
        # B, 2k
        return theta

    def forward(self, x):
        raise NotImplementedError


class LangDyReLUA(LangDyReLU):
    def __init__(self, q_dim, channels, reduction=4, k=2, conv_type='2d'):
        super(LangDyReLUA, self).__init__(q_dim, channels, reduction, k, conv_type)
        self.fc2 = nn.Linear(channels // reduction, 2*k)

    def forward(self, x, q):
        assert x.shape[1] == self.channels
        theta = self.get_relu_coefs(q)
        # B,2k
        relu_coefs = theta.view(-1, 2*self.k) * self.lambdas + self.init_v
        # BxCxL -> LxCxBx1
        x_perm = x.transpose(0, -1).unsqueeze(-1)
        output = x_perm * relu_coefs[:, :self.k] + relu_coefs[:, self.k:]
        # LxCxBx2 -> BxCxL
        result = torch.max(output, dim=-1)[0].transpose(0, -1)

        return result


class LangDyReLUB(LangDyReLU):
    def __init__(self, q_dim, channels, reduction=4, k=2, conv_type='2d'):
        super(LangDyReLUB, self).__init__(q_dim, channels, reduction, k, conv_type)
        self.fc2 = nn.Linear(channels // reduction, 2*k*channels)

    def forward(self, x, q):
        assert x.shape[1] == self.channels
        theta = self.get_relu_coefs(q)

        relu_coefs = theta.view(-1, self.channels, 2*self.k) * self.lambdas + self.init_v

        if self.conv_type == '1d':
            # BxCxL -> LxBxCx1
            x_perm = x.permute(2, 0, 1).unsqueeze(-1)
            output = x_perm * relu_coefs[:, :, :self.k] + relu_coefs[:, :, self.k:]
            # LxBxCx2 -> BxCxL
            result = torch.max(output, dim=-1)[0].permute(1, 2, 0)

        elif self.conv_type == '2d':
            # BxCxHxW -> HxWxBxCx1
            x_perm = x.permute(2, 3, 0, 1).unsqueeze(-1)
            output = x_perm * relu_coefs[:, :, :self.k] + relu_coefs[:, :, self.k:]
            # HxWxBxCx2 -> BxCxHxW
            result = torch.max(output, dim=-1)[0].permute(2, 3, 0, 1)

        return result


class LangDyReLUC(LangDyReLU):
    def __init__(self, q_dim, channels, reduction=4, k=2, conv_type='1d'):
        super(LangDyReLUC, self).__init__(q_dim, channels, reduction, k, conv_type)
        self.fc2 = nn.Linear(channels // reduction, 2*k*channels)

    def forward(self, x, q):
        """
        :param x: B, LEN, X_DIM
        :param q: B, Q_DIM
        :return:
        """
        assert x.shape[2] == self.channels
        theta = self.get_relu_coefs(q)

        relu_coefs = theta.view(-1, self.channels, 2*self.k) * self.lambdas + self.init_v

        if self.conv_type == '1d':
            # BxLxC -> LxBxCx1
            x_perm = x.permute(1, 0, 2).unsqueeze(-1)
            output = x_perm * relu_coefs[:, :, :self.k] + relu_coefs[:, :, self.k:]
            # LxBxCx2 -> BxCxL
            result = torch.max(output, dim=-1)[0].permute(1, 0, 2)

        return result


